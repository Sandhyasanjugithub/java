package strings1;

public class UsingLiteralAndNewKeyword {

	public static void main(String[] args) {
		String s=new String("hyd");
		String s1="hyd";
		String s2="bng";
		String s3=new String("tyss");
		System.out.println(s==s1);
		System.out.println(s.equals(s3));
		System.out.println(s==s1);
	}

}
